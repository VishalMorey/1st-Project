package logicalProgram;

public class ProgramEG1 {

	public static void main(String[] args) {
		//5row   6 columns
		
//		******
//		******
//		******
//		******
//		******
		//--row 5 star=6
		int star=6;
		
		for(int a=1;a<=5;a++)
		{ 
			for(int b=1;b<=star;b++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
	}
}
